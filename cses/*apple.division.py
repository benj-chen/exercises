n=int(input())
l=input().split()


s=int(max(l))
for x in range(1,2**n//2):
    c=str(bin(x))[2:].zfill(n)
    a=0
    for y in range(len(c)):
        if c[y]=="0":a+=int(l[y])
        else: a-=int(l[y])
    s=min(abs(a),s)
    if s==0: break
print(s)

